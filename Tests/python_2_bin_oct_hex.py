numOne = int(input("Give me an integer? "))
numTwo = int(input("Give me another integer? "))
sum = numOne + numTwo

print(f"Your first number in binary is {bin(numOne)}.")
print(f"Your first number in octal is {oct(numOne)}.")
print(f"Your first number in hexadecimal is {hex(numOne)}.")
print(f"Your second number in binary is {bin(numTwo)}.")
print(f"Your second number in octal is {oct(numTwo)}.")
print(f"Your second number in hexadecimal is {hex(numTwo)}.")
print(f"The sum of your two numbers in decimal is {sum}.")
print(f"The sum of your two numbers in binary is {bin(sum)}.")
print(f"The sum of your two numbers in octal is {oct(sum)}.")
print(f"The sum of your two numbers in hexadecimal is {hex(sum)}.")